#include <stdio.h>

/*
 * A program which prints the daily life of a programmer
 * while alive
 */

int main(void);

{

int breathing = 1; /* Initialize breathing true */

while (breathing)
{

printf("Eat\n");
printf("Code\n");
printf("Sleep\n");
printf("Code\n");
breathing = 0; /* End the loop after one iteration */
}
return (0);
}
